    private void tb_personasMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_personasMouseReleased
        // TODO add your handling code here:
        if (evt.getButton() == 3) {
            int numero_fila = tb_personas.rowAtPoint(evt.getPoint());
            ListSelectionModel lsm = tb_personas.getSelectionModel();
            lsm.setSelectionInterval(numero_fila, numero_fila);
            pm_clickderecho.show(evt.getComponent(), evt.getX(), evt.getY());
        } else {
            if (evt.getClickCount() >= 2) {
                int fila = tb_personas.getSelectedRow();
                verPersona(fila);
            }
        }
    }//GEN-LAST:event_tb_personasMouseReleased

