    //Buscar Persona segun id
    public void verPersona(int row){
        int idPersona = Integer.parseInt(""+(tb_personas.getValueAt(row, 0)));
        blp = blp.personaPorId(idPersona, listaPersonas);
        AgregarPersona ag = new AgregarPersona(this, rootPaneCheckingEnabled, blp);
        ag.setVisible(true);
    }
    
    //filtro busqueda sencible
    public void filtrar() {
        String hilera = tf_buscarPersona.getText();
        if (!hilera.isEmpty() && hilera.charAt(0) >= 97) {
            hilera = (char) ((hilera.charAt(0) - 32)) + hilera.substring(1);
        }
        trsfiltro.setRowFilter(RowFilter.regexFilter(hilera, 1));
    }

    //Metodos para cargar los datos necesarios
    public void cargarPersonas() {
        blp = new BL_Persona();
        listaPersonas = blp.cargarPersonas();
        formatoTablaPersonas();
    }

    //Metodo definir formato de tablas
    public void formatoTablaPersonas() {

        String[] nombreColumnas = {"id","Nombre", "Primer Apellido", "Segundo Apellido"};
        dtmpersonas = new DefaultTableModel(null, nombreColumnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        if (!listaPersonas.isEmpty()) {
            for (BL_Persona persona : listaPersonas) {
                dtmpersonas.addRow(new Object[]{persona.getIdPersona(),persona.getNombre(), persona.getPrimerApellido(),
                    persona.getSegundoApellido()});
            }
        }
        tb_personas.setModel(dtmpersonas);
        tb_personas.getColumnModel().getColumn(0).setMinWidth(0);
        tb_personas.getColumnModel().getColumn(0).setMaxWidth(0);
        tb_personas.getColumnModel().getColumn(0).setWidth(0);
        trsfiltro = new TableRowSorter(tb_personas.getModel());
    }

